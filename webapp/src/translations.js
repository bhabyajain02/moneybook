// Fallback to hinglish if language not found, then to key itself
export function t(key, language = "hinglish") {
  const lang = TRANSLATIONS[language] || TRANSLATIONS.hinglish;
  return lang[key] ?? TRANSLATIONS.hinglish[key] ?? key;
}

// Get a translated label for a tag name (e.g. "staff_expense" -> "स्टाफ खर्च" in Hindi)
export function tagLabel(tag, language = "hinglish") {
  const key = `tag_${tag}`;
  const translated = t(key, language);
  // If no translation found (returns the key itself), fall back to formatted tag name
  if (translated === key) {
    return tag.replace(/_/g, " ").replace(/\b\w/g, c => c.toUpperCase());
  }
  return translated;
}

